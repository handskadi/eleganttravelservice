export function resolveCanonicalTourSlug(rawSlug: string): string {
  const s = decodeURIComponent(rawSlug).normalize("NFC");
  if (tourSlugs[s]) return s;
  for (const [canonical, locales] of Object.entries(tourSlugs)) {
    if (Object.values(locales).some((ls) => ls?.normalize("NFC") === s)) return canonical;
  }
  return s;
}

export function getTourSlug(canonicalId: string, locale: string): string {
  return tourSlugs[canonicalId]?.[locale as Locale] ?? canonicalId;
}
